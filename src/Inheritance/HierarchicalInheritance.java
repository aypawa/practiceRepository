package Inheritance;

public class HierarchicalInheritance {

	public static void main(String[] args) {

		Ajinkya A =new Ajinkya();
		Bhaskar B=new Bhaskar();
		Yogesh Y=new Yogesh();
		
		A.spm();
		A.sdi();
		
		B.spm();
		B.sdi();
		B.sp();
		B.asp();
		
		Y.spm();
		Y.sdi();
		Y.pa();
		Y.postman();
		
		
	}

}
