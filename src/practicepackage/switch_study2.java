package practicepackage;

public class switch_study2 {

	public static void main(String[] args) {

		int day=4;
		
		switch(day)
		{
		case 1:
			System.out.println("this is the monday");
			break;
		case 2:
			System.out.println("this is the tuesday");
			break;
		case 3:
			System.out.println("this is the wednesday");
			break;
		case 4:
			System.out.println("this is the thursday");
			break;
		case 5:
			System.out.println("this is the friday");
			break;
		case 6:
			System.out.println("this is the saturday");
			break;
		case 7:
			System.out.println("this is the saturday");
			break;
		default:
			System.out.println("this is the invalid week of day");
		}
		
	}

}
