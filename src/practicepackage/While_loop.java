package practicepackage;

public class While_loop {

	public static void main(String[] args) {

		int i=1;
		while(i<=10)
		{
			System.out.println("Pune");
		}

		int j=2;
		while(j<=20)
		{
			System.out.println(j);
			j=j+2;
		}
		int k=50;
		while(k>=5)
		{
			System.out.println(k);
			k=k-5;
		}
		
		
	}

}
